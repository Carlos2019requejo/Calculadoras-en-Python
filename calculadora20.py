#Calcular el area de un rectoedro
area, ancho, largo, altura=0.0, 0.0, 0.0, 0.0

#Asignacion de valores
ancho=4
largo=5
altura=8

#Calculo
area=((ancho+largo)+(largo+altura)+(ancho+altura))*2
#Mostrar valores
print ("ancho", ancho)
print ("altura", altura)
print ("largo", largo)
print ("area del rectoedro", area)
